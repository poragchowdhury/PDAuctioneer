package TacTex;

public class ChargeMwhPair {
	  
	  double charge;
	  double mwh;
	  /**
	   * @param charge
	   * @param mwh
	   */
	  public ChargeMwhPair(double charge, double mwh) {
	    super();
	    this.charge = charge;
	    this.mwh = mwh;
	  }
	  
	  public double getCharge() {
	    return charge;
	  }
	  
	  public double getMwh() {
	    return mwh;
	  }
}

